//1. 手机号码
let regPhone = /^1[3456789]\d{9}$/;


//2. 身份证
let regCardID = /^[1-9]\d{5}(18|19|([23]\d))\d{2}((0[1-9])|(10|11|12))(([0-2][1-9])|10|20|30|31)\d{3}[0-9Xx]$/;

//3. 邮箱
let regEmail = /^([a-zA-Z0-9_-])+@([a-zA-Z0-9_-])+((\.[a-zA-Z0-9_-]{2,3}){1,2})$/;

//4. 汉字
let regHanzi = /[\u4E00-\u9FA5]/g;

//4. 非汉字
let regNoHanzi = /[^\u4E00-\u9FA5]/g;